cadena = 'Estoy utilizando los metodos de Python'

print(cadena.lower()) #lower transforma todas las letras en minusculas
print(cadena.upper()) #upper transforma todas las letras en mayusculas
print(cadena.capitalize()) #solo muestra la primer letra en mayusculas
print(cadena.title()) # Convierte cada inicial en mayuscula
print(cadena.swapcase()) #ivierte mayusculas y minusculas

