cadena = "Este es un ejemple de substring (Debanado de Cadenas)"

print(len(cadena)) #len cuenta el numero de caracteres del string

print(cadena[2]) #busca dentro de la cadena el 2do caracter (0, 1, 2)

print(cadena[0:9]) #es numero de la derecha nunca va a ser tomado en cuenta (0 incluido, 9 no incluido)

print(cadena[-3:]) #cuando el nuemro es negativo la cuenta inicia desde atras


