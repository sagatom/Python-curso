#primero importar librerias
import math
import random

print(math.pow(10 , 2)) #pow eleva un numero a un exponente. 10 al cuadrado en este caso
print(math.pow(2 , 3))
print(math.sqrt(64)) #sqrt hace la raiz cuadrada
print(math.isqrt(64)) #raiz cuadrada en numero entero
print(math.sin(90)) #seno de 90
print(math.cos(90)) #coseno de 90
print(math.tan(90)) #tangente 
print(math.factorial(10)) 

print(random.randint(1 , 100))