print(lista.count(5))     #cuanta cuantos items se repiten en la lista

print(lista.index(4))      #busca elementos en la lista el valor que coloco y devuelve la posicion

lista.sort()               #sort no lleva parametro y ordena la lista. y debe aplicarse antes del print
print(lista)

lista.reverse()             #muestra la lista de atras hacia adelante

